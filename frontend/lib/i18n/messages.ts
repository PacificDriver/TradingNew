/** Optional placeholder map, e.g. { n: "5" } for "…{n} записей" */
export function getMessage(locale: Locale, key: string, placeholders?: Record<string, string | number>): string {
  let msg = messages[locale]?.[key];
  if (msg == null) msg = messages[DEFAULT_LOCALE]?.[key] ?? key;
  if (placeholders) {
    for (const [k, v] of Object.entries(placeholders)) {
      msg = msg.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return msg;
}

